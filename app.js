const video = document.querySelector('.video-background');

const swiperText = new Swiper('.swiper', {
  direction: 'vertical',
  speed: 2000,
  simulateTouch: false,
  pagination: {
    el: '.swiper-pagination',
    clickable: true
  },
  navigation: {
    prevEl: '.swiper-button-prev',
    nextEl: '.swiper-button-next'
  },
  loop: false,
  // autoplay: {
  //   delay: 5000,
  //   disableOnInteraction: true
  // }
});
// Функция для обновления и запуска видео
function updateVideoTime() {
  video.currentTime = (video.duration / swiperText.slides.length) * swiperText.realIndex;
  video.play(); // Запустить воспроизведение видео
}

// Обработчик события для окончания видео
video.addEventListener('ended', () => {
  video.currentTime = 0; // Устанавливаем время на начало
  video.play(); // Запускаем воспроизведение заново
});

// Обработка смены слайда
swiperText.on('slideChange', function() {
  updateVideoTime(); // Устанавливаем видео в соответствующее время при смене слайда
});

// Обработка событий для начала воспроизведения видео при загрузке
video.addEventListener('loadedmetadata', () => {
  updateVideoTime(); // Устанавливаем текущее время видео при загрузке
});

// Добавление классов для анимации при переходах
swiperText.on('slideChangeTransitionStart', function() {
  video.classList.add('change');
}).on('slideChangeTransitionEnd', function() {
  video.classList.remove('change');
});

// Обработчик событий для клавиатуры
document.addEventListener('keydown', function(event) {
  if (event.key === 'ArrowUp') {
    swiperText.slidePrev(); // Переход к предыдущему слайду
    updateVideoTime(); // Обновляем видео при переходе на предыдущий слайд
  } else if (event.key === 'ArrowDown') {
    swiperText.slideNext(); // Переход к следующему слайду
    updateVideoTime(); // Обновляем видео при переходе на следующий слайд
  }
});


