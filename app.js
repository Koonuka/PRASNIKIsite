var btn = document.getElementById("myBtn");
var span = document.getElementsByClassName("close")[0];

btn.onclick = function() {
    // Устанавливаем display перед тем, как показать
    modal.style.display = "block"; 
    setTimeout(function() {
        modal.classList.add("show"); // Добавляем класс, чтобы анимация сработала
    }, 10); // Небольшая задержка для того, чтобы класс применился после изменения стиля
}

span.onclick = function() {
    closeModal();
}

window.onclick = function(event) {
    if (event.target == modal) {
        closeModal();
    }
}

document.addEventListener('keydown', function(event) {
    if (event.key === "Escape") { // Закрытие модального окна по клавише ESC
        closeModal();
    }
});

