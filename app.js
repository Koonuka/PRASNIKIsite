document.getElementById('search-button').addEventListener('click', function() {
    const countryInput = document.getElementById('country-search').value.trim();
    const modal = document.getElementById('modal');
    const resultsDiv = document.getElementById('results');
    const countryTitle = document.getElementById('country-title');
    const prevButton = document.getElementById('prev-button');
    const nextButton = document.getElementById('next-button');

    resultsDiv.innerHTML = ''; // Очистка предыдущих результатов
    currentPage = 1; // Сброс текущей страницы

    // Фильтруем праздники по стране
    const filteredHolidays = holidays.filter(holiday => 
        holiday.country.toLowerCase() === countryInput.toLowerCase()
    );

    if (filteredHolidays.length === 0) {
        // Если праздники не найдены, выводим сообщение и закрываем модальное окно
        alert("Праздники не найдены для указанной страны. Пожалуйста, попробуйте другую страну.");
        modal.style.display = "none"; // Закрываем модальное окно, если нет праздников
        return; // Прекращаем выполнение функции
    }

    // Сортируем праздники по дате
    filteredHolidays.sort((a, b) => new Date(a.date) - new Date(b.date));

    const totalResults = filteredHolidays.length;
    const totalPages = Math.ceil(totalResults / resultsPerPage);
    
    // Устанавливаем заголовок с названием страны
    countryTitle.textContent = `Праздники в стране ${countryInput.charAt(0).toUpperCase() + countryInput.slice(1)}`;

    // Показываем результаты текущей страницы
    renderResults(filteredHolidays, totalPages);

    // Управляем кнопками
    prevButton.style.display = currentPage > 1 ? 'block' : 'none';
    nextButton.style.display = currentPage < totalPages ? 'block' : 'none';

    // Открываем модальное окно
    modal.style.display = "block";

    // Управление кнопками "Следующая" и "Предыдущая"
    nextButton.onclick = function() {
        currentPage++;
        renderResults(filteredHolidays, totalPages);
    };

    prevButton.onclick = function() {
        currentPage--;
        renderResults(filteredHolidays, totalPages);
    };
});

